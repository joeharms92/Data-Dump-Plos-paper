ansiCNRPrimaryGeneratorAction::ansiCNRPrimaryGeneratorAction(ansiCNRDetectorConstruction* detector,
							G4String beamType, G4int sliceNo)
: 	G4VUserPrimaryGeneratorAction(),
	fParticleGun(0),
	fDet(detector),
	fSliceNum(sliceNo)
{
	G4int nofParticles = 1;
	fParticleGun = new G4ParticleGun(nofParticles);

	G4ParticleDefinition* particleDefinition 
		= G4ParticleTable::GetParticleTable()->FindParticle("gamma");

	fParticleGun->SetParticleDefinition(particleDefinition);	

	if ( (beamType == "ICS") || (beamType == "ics") ) {
		energy = GetICSEnergyDist();
		prob = GetICSProbDist();
	}
	else if ( (beamType == "LENR") || (beamType == "lenr") ) {
		energy = GetLENREnergyDist();
		prob = GetLENRProbDist();
	}
	else if ( (beamType == "Brem4") || (beamType == "brem4") ) {
		energy = GetBremEnergyDist4();
		prob = GetBremProbDist4();
	}
	else if ( (beamType == "Brem6") || (beamType == "brem6") ) {
		energy = GetBremEnergyDist6();
		prob = GetBremProbDist6();
	}
	else if ( (beamType == "Brem9") || (beamType == "brem9") ) {
		energy = GetBremEnergyDist9();
		prob = GetBremProbDist9();
	}
	else if ( (beamType == "Brem15") || (beamType == "brem15") ) {
		energy = GetBremEnergyDist9();
		prob = GetBremProbDist9();
	}
	else if ( (beamType == "PuBe") || (beamType == "pube") ) {
		energy = GetPuBeEnergyDist();
		prob = GetPuBeProbDist();
	}
	else{
		G4cout << "No beam type specified" << G4endl;
		G4cout << "Default beam is PuBe" << G4endl;
		energy = GetPuBeEnergyDist();
		prob = GetPuBeProbDist();
  	}

}

ansiCNRPrimaryGeneratorAction::~ansiCNRPrimaryGeneratorAction()
{
	delete fParticleGun;
	delete[] energy;
	delete[] prob;
}

void ansiCNRPrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{
	G4double probRand = G4UniformRand();
	G4int counter = 0;
	while ( probRand > prob[counter]){
		counter++;
	}

	fParticleGun->SetParticleEnergy(energy[counter]);

	G4ThreeVector worldDims = fDet->GetWorldDims();

	G4double posY = worldDims.y()* (G4UniformRand() - 0.5);
    
	G4double posX = -worldDims.x()/2. + 3*mm * (G4double)fSliceNum + 6*mm*G4UniformRand();

	fParticleGun->SetParticlePosition(G4ThreeVector(posX,posY,-worldDims.z()/2.));
	fParticleGun->SetParticleMomentumDirection(G4ThreeVector(0,0,1));
	fParticleGun->GeneratePrimaryVertex(anEvent);
}

